const SIDEBAR_WIDTH = "16rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"

type SidebarContext = {
  state: "expanded" | "collapsed"
  open: boolean
  setOpen: (open: boolean) => void
  openMobile: boolean
  setOpenMobile: (open: boolean) => void
  isMobile: boolean
  toggleSidebar: () => void
}

const SidebarContext = React.createContext<SidebarContext | undefined>(
  undefined
)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }
  return context
}

interface SidebarProviderProps extends React.ComponentProps<"div"> {
  defaultOpen?: boolean
  open?: boolean
  onOpenChange?: (open: boolean) => void
}

const SidebarProvider = React.forwardRef<
  HTMLDivElement,
  SidebarProviderProps
>(({ defaultOpen = true, open: openProp, onOpenChange: setOpenProp, className, style, children, ...props }, ref) => {
  const [_open, _setOpen] = React.useState(defaultOpen)
  const [openMobile, setOpenMobile] = React.useState(false)
  const [isMobile, setIsMobile] = React.useState(false)

  const open = openProp ?? _open
  const setOpen = React.useCallback(
    (value: boolean | ((value: boolean) => boolean)) => {
      const openState = typeof value === "function" ? value(open) : value
      if (setOpenProp) {
        setOpenProp(openState)
      } else {
        _setOpen(openState)
      }
    },
    [setOpenProp, open]
  )

  const toggleSidebar = React.useCallback(() => {
    return isMobile ? setOpenMobile((open) => !open) : setOpen((open) => !open)
  }, [isMobile, setOpen, setOpenMobile])

  React.useEffect(() => {
    const checkMobile = () => {
      setIsMobile(window.innerWidth < 768)
    }
    checkMobile()
    window.addEventListener("resize", checkMobile)
    return () => window.removeEventListener("resize", checkMobile)
  }, [])

  const state = open ? "expanded" : "collapsed"

  const contextValue = React.useMemo<SidebarContext>(
    () => ({
      state,
      open,
      setOpen,
      isMobile,
      openMobile,
      setOpenMobile,
      toggleSidebar,
    }),
    [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar]
  )

  return (
    <SidebarContext.Provider value={contextValue}>
      <div
        style={{
          "--sidebar-width": SIDEBAR_WIDTH,
          "--sidebar-width-mobile": SIDEBAR_WIDTH_MOBILE,
          ...style,
        } as React.CSSProperties}
        className={cn(
          "group/sidebar-wrapper flex min-h-svh w-full has-[[data-variant=inset]]:bg-sidebar",
          className
        )}
        ref={ref}
        {...props}
      >
        {children}
      </div>
    </SidebarContext.Provider>
  )
})
SidebarProvider.displayName = "SidebarProvider"

const sidebarVariants = cva(
  "group peer hidden md:block text-sidebar-foreground md:h-svh md:w-[--sidebar-width] md:border-r",
  {
    variants: {
      variant: {
        sidebar: "md:bg-sidebar md:border-sidebar-border",
        floating: "md:bg-sidebar md:border-sidebar-border md:border",
        inset: "md:bg-background",
      },
      side: {
        left: "",
        right: "order-last",
      },
    },
    defaultVariants: {
      side: "left",
      variant: "sidebar",
    },
  }
)

interface SidebarProps extends React.ComponentProps<"div">, VariantProps<typeof sidebarVariants> {
  side?: "left" | "right"
}

const Sidebar = React.forwardRef<HTMLDivElement, SidebarProps>(
  ({ side = "left", variant = "sidebar", className, children, ...props }, ref) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    if (isMobile) {
      return (
        <div
          className={cn(
            "fixed inset-0 z-50 md:hidden",
            openMobile ? "block" : "hidden"
          )}
        >
          <div
            className="fixed inset-0 bg-black/50"
            onClick={() => setOpenMobile(false)}
          />
          <div
            ref={ref}
            className={cn(
              "fixed h-svh w-[--sidebar-width-mobile] bg-sidebar p-0 text-sidebar-foreground shadow-md",
              side === "left" ? "left-0" : "right-0",
              className
            )}
            {...props}
          >
            <div className="flex h-full w-full flex-col">{children}</div>
          </div>
        </div>
      )
    }

    return (
      <div
        ref={ref}
        data-state={state}
        data-side={side}
        className={cn(sidebarVariants({ side, variant }), className)}
        {...props}
      >
        <div className="flex h-full w-full flex-col">{children}</div>
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

